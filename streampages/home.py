import streamlit as st

def main():
    # ✅ 홈 화면 UI
    st.title("🏠 맞춤형 지원사업 추천 서비스")
    st.write("""
        본 서비스는 사용자의 사업 정보를 입력받아 **맞춤형 정부 지원사업**을 추천해주는 시스템입니다.
        
        좌측 사이드바에서 **"추천 시스템"**을 선택하여 지원사업 추천을 받아보세요!
    """)

    # ✅ 로그인 여부 확인
    if "logged_in" in st.session_state and st.session_state["logged_in"]:
        st.success(f"환영합니다, {st.session_state['user']['name']}님! 😊")
        st.write("👉 추천 시스템에서 맞춤 지원사업을 확인하세요.")
    else:
        st.warning("로그인이 필요합니다. 로그인 후 추천 서비스를 이용하세요!")

    # ✅ 서비스 기능 설명
    st.subheader("📌 서비스 주요 기능")
    st.markdown("""
    - **사업 정보 입력**: 사업 유형, 업종, 지역 등 기본 정보를 입력합니다.
    - **맞춤 지원사업 추천**: 입력한 정보를 바탕으로 관련 지원사업을 찾아드립니다.
    - **간편한 사용**: 불필요한 입력을 최소화하여 빠르게 추천을 받을 수 있습니다.
    """)

    # ✅ 사용 방법 안내
    st.subheader("🛠️ 사용 방법")
    st.write("""
    1. 좌측 **사이드바**에서 `추천 시스템` 메뉴를 선택합니다.
    2. 사업 정보를 입력하고 `제출` 버튼을 누릅니다.
    3. 본인에게 맞는 지원사업 리스트를 확인하세요!
    """)

if __name__ == "__main__":
    main()


